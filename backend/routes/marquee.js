function getGlyph(ch) {
  const code = ch.charCodeAt(0);
  const idx  = (code >= 32 && code <= 126) ? (code - 32) : 0;
  return FONT8.slice(idx * 8, idx * 8 + 8);
}

function hexToRgb(hex) {
  const v = (hex || '#000000').replace('#','');
  return [parseInt(v.slice(0,2),16)||0, parseInt(v.slice(2,4),16)||0, parseInt(v.slice(4,6),16)||0];
}

function measureText(text) { return text.length * (8 * SCALE + SCALE); }

function drawFrame(text, offsetX, fgRgb, bgRgb, w, h) {
  const buf = new Uint8Array(w * h * 4);
  for (let i = 0; i < w * h; i++) {
    buf[i*4]=bgRgb[0]; buf[i*4+1]=bgRgb[1]; buf[i*4+2]=bgRgb[2]; buf[i*4+3]=255;
  }
  for (let y = 0; y < h; y++) {
    for (let x = 0; x < 4; x++) {
      const i=(y*w+x)*4; buf[i]=fgRgb[0]; buf[i+1]=fgRgb[1]; buf[i+2]=fgRgb[2]; buf[i+3]=255;
    }
  }
  const charW  = 8 * SCALE + SCALE;
  const textTop = Math.floor((h - 8 * SCALE) / 2);
  let cx = offsetX;
  for (const ch of text) {
    const glyph = getGlyph(ch);
    for (let row = 0; row < 8; row++) {
      const bits = glyph[row];
      for (let col = 0; col < 8; col++) {
        if (bits & (0x80 >> col)) {
          for (let sy = 0; sy < SCALE; sy++) {
            for (let sx = 0; sx < SCALE; sx++) {
              const px = cx + col*SCALE + sx, py = textTop + row*SCALE + sy;
              if (px >= 0 && px < w && py >= 0 && py < h) {
                const i=(py*w+px)*4; buf[i]=fgRgb[0]; buf[i+1]=fgRgb[1]; buf[i+2]=fgRgb[2]; buf[i+3]=255;
              }
            }
          }
        }
      }
    }
    cx += charW;
  }
  return buf;
}

function buildGIF(text, tc, bg) {
  const fgRgb = hexToRgb(tc);
  const bgRgb = hexToRgb(bg);
  const textW = measureText(text);
  const totalScroll = W + textW + 40;
  const step = totalScroll / FRAMES;

  const encoder = new GIFEncoder(W, H, 'neuquant', true);
  encoder.setDelay(DELAY);
  encoder.setRepeat(0);
  encoder.start();

  for (let i = 0; i < FRAMES; i++) {
    const offsetX = Math.floor(W - step * i);
    encoder.addFrame(drawFrame(text, offsetX, fgRgb, bgRgb, W, H));
  }
  encoder.finish();
  return Buffer.from(encoder.out.getData());
}

// GET /api/marquee/gif?text=Hello&tc=e8820a&bg=fff8ee
router.get('/gif', (req, res) => {
  const text = String(req.query.text || 'Scrolling Text').slice(0, 120);
  const tc   = '#' + String(req.query.tc  || 'e8820a').replace('#','').slice(0,6);
  const bg   = '#' + String(req.query.bg  || 'fff8ee').replace('#','').slice(0,6);

  try {
    const gif = buildGIF(text, tc, bg);
    res.setHeader('Content-Type', 'image/gif');
    res.setHeader('Cache-Control', 'public, max-age=3600');
    res.send(gif);
  } catch(e) {
    console.error('[Marquee GIF]', e.message);
    res.status(500).send('GIF generation failed');
  }
});

